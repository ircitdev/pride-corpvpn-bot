from aiogram import Router, types, F

router = Router()

@router.message(F.text == "📘 Правила и инструкции")
async def show_rules(message: types.Message):
    text = (
        "📘 Инструкция по подключению VPN:\n\n"
        "1. Нажми «🎁 Получить тест-доступ».\n"
        "2. Скопируй выданную ссылку (vless://...)\n"
        "3. Открой приложение VPN:\n"
        "   • Android: v2rayNG\n"
        "   • iOS: NapsternetV / Streisand / Happ Plus\n"
        "   • Windows / macOS: Clash / Nekoray\n"
        "4. Импортируй ссылку.\n"
        "5. Включи VPN.\n\n"
        "⚠ ВАЖНО: не передавай свою ссылку другим людям. Нарушения фиксируются автоматикой.\n"
        "За подозрительную активность мы можем заблокировать доступ."
    )
    await message.answer(text)

